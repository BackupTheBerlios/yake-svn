#include <yakeBase/yake/templates/yakeSingleton.h>

using namespace yake::base::Templates::Detail;

yake::base::Templates::Detail::TrackerArray yake::base::Templates::Detail::pTrackerArray = 0;
unsigned int yake::base::Templates::Detailelements = 0;


void yake::base::Templates::Detail::AtExitFn()
{
    assert(elements > 0 && pTrackerArray != 0);
    // Pick the element at the top of the stack
    LifetimeTracker* pTop = pTrackerArray[elements - 1];
    // Remove that object off the stack
    // Don't check errors - realloc with less memory 
    //     can't fail
    pTrackerArray = static_cast<TrackerArray>(std::realloc(
        pTrackerArray, sizeof(*pTrackerArray) * --elements));
    // Destroy the element
    delete pTop;
}